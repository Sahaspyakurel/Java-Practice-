class ArithmeticOp{
   public static void main (String[]args){
     int a=10, b=2, p,q,r,s,t;
     p=a+b;
     System.out.println("a+b ="+p);
     q=a-b;
     System.out.println("a-b ="+q);
     r=a*b;
     System.out.println("a*b ="+r);
     s=a/b;
     System.out.println("a/b ="+s);
     t=a%b;
     System.out.println("a%b ="+t);

   }

}